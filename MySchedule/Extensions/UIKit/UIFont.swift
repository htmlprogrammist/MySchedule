import UIKit

extension UIFont {
    // Avenir Next
    static func avenirNext20() -> UIFont? {
        return UIFont(name: "Avenir Next", size: 20)
    }
    
    static func avenirNext14() -> UIFont? {
        return UIFont(name: "Avenir Next", size: 14)
    }
    
    // Avenir Next Demi Bold
    static func avenirNextDemiBold20() -> UIFont? {
        return UIFont(name: "Avenir Next Demi Bold", size: 20)
    }
    
    static func avenirNextDemiBold14() -> UIFont? {
        return UIFont(name: "Avenir Next Demi Bold", size: 14)
    }
}
